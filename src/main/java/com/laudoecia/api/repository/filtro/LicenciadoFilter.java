package com.laudoecia.api.repository.filtro;

public class LicenciadoFilter {
	private String cnpj;
	private String cnes;
	private String cpf;
	private String licenciadopara;
	private String razaosocial;
	private String email;
	private String site;

	public String getCnpj() {
		return cnpj;
	}

	public void setCnpj(String cnpj) {
		this.cnpj = cnpj;
	}

	public String getCnes() {
		return cnes;
	}

	public void setCnes(String cnes) {
		this.cnes = cnes;
	}

	public String getCpf() {
		return cpf;
	}

	public void setCpf(String cpf) {
		this.cpf = cpf;
	}

	public String getLicenciadopara() {
		return licenciadopara;
	}

	public void setLicenciadopara(String licenciadopara) {
		this.licenciadopara = licenciadopara;
	}

	public String getRazaosocial() {
		return razaosocial;
	}

	public void setRazaosocial(String razaosocial) {
		this.razaosocial = razaosocial;
	}

	public String getEmail() {
		return email;
	}

	public void setEmail(String email) {
		this.email = email;
	}

	public String getSite() {
		return site;
	}

	public void setSite(String site) {
		this.site = site;
	}

}
